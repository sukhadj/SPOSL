package com.sukhad;

public class KPDT {

	String parameter;
	String value;

	public KPDT(String parameter, String value) {
		super();
		this.parameter = parameter;
		this.value = value;
	}

	@Override
	public String toString() {
		return "KPDT [parameter=" + parameter + ", value=" + value + "]";
	}
	
	
	
}
